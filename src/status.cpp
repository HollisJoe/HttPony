static const char* status_message(unsigned code)
{
    auto it = names.find(code);
    return it == names.end() ? "" : it->second;
}

Status::Status(StatusCode status)
    : Status(unsigned(status))
{
}

Status::Status(unsigned code)
    : code(code), message(status_message(code))
{
}

Status::Status(unsigned code, std::string message)
    : code(code), message(std::move(message))
{
}

StatusType Status::type() const
{
    if ( code >= 600 )
        return StatusType::Invalid;
    if ( code >= 500 )
        return StatusType::ServerError;
    if ( code >= 400 )
        return StatusType::ClientError;
    if ( code >= 300 )
        return StatusType::Redirection;
    if ( code >= 200 )
        return StatusType::Success;
    if ( code >= 100 )
        return StatusType::Informational;
    return StatusType::Invalid;
}

bool Status::is_error() const
{
    return code < 100 || code >= 400;
}

